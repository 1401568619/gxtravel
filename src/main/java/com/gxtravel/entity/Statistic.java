package com.gxtravel.entity;

public class Statistic {
    private String theme;
    private Integer num;

    public Statistic() {
    }

    public void setTheme(String theme) {
        this.theme = theme;
    }

    public String getTheme() {
        return theme;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public Integer getNum() {
        return num;
    }

}
